import UIKit

extension UIColor {
    
    static func textColor() -> UIColor {
        return #colorLiteral(red: 0.1503021728, green: 0.1627354471, blue: 0.1807979061, alpha: 1)
    }
    
    static func cellGrayColor() -> UIColor{
        return #colorLiteral(red: 0.9725490196, green: 0.9725490196, blue: 0.9725490196, alpha: 1)
    }
    
    static func cellWhiteColor() -> UIColor{
        return .white
    }
}
